public class Main {
    // function name has to be main only
    // public :- access modifier
    // static :- no need to create object to call main function
    // void :- return type that means main function will not return anything
    // String[] args :- command line arguments are passed as array of strings
    public static void main(String[] args) {

        System.out.println(args[0]);
    }
}